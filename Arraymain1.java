public class Arraymain1 {
    public static void main(String args[]) {
        
        // Initializing array with elements
        int[] arr = {1, 2, 3, 4, 5, 6};

        // Printing array elements
        System.out.print("Array elements: ");
        for (int i = 0; i < arr.length; i++) {
            System.out.print(arr[i] + " ");
        }
    }
}